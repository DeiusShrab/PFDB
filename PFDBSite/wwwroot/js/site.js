function openNav() {
    document.getElementById("sidenavclose").style.width = "0";
    document.getElementById("sidenav").style.width = "250px";
}

/* Set the width of the side navigation to 0 */
function closeNav() {
    document.getElementById("sidenav").style.width = "0";
    document.getElementById("sidenavclose").style.width = "25px";
}
